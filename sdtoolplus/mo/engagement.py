from datetime import datetime

import structlog.stdlib
from more_itertools import first
from more_itertools import last
from more_itertools import only

from sdtoolplus.autogenerated_graphql_client import EngagementUpdateInput
from sdtoolplus.depends import GraphQLClient
from sdtoolplus.exceptions import EngagementNotFoundError
from sdtoolplus.exceptions import PersonNotFoundError
from sdtoolplus.mo.timeline import get_mo_validity
from sdtoolplus.mo.timeline import get_update_validity
from sdtoolplus.models import Active
from sdtoolplus.models import EngagementMovePayload
from sdtoolplus.models import Timeline
from sdtoolplus.sd.timeline import sd_end_datetime
from sdtoolplus.sd.timeline import sd_start_datetime
from sdtoolplus.timeline import prefix_user_key_with_inst_id

logger = structlog.stdlib.get_logger()


def _is_active_in_entire_interval(
    timeline: Timeline[Active], start: datetime, end: datetime
) -> bool:
    assert start < end
    if timeline == Timeline[Active]():
        return False
    if start < first(timeline.intervals).start:
        return False
    if end > last(timeline.intervals).end:
        return False
    # If this is not the case there must be a hole in the timeline
    return len(timeline.intervals) == 1


async def move_engagement(
    gql_client: GraphQLClient, payload: EngagementMovePayload, dry_run: bool = False
) -> None:
    """
    Move engagement to the location specified in the payload in the given time period.
    """
    logger.info("Move engagement", payload=payload.dict())

    user_key = prefix_user_key_with_inst_id(
        payload.employment_identifier, payload.institution_identifier
    )

    payload_validity = get_mo_validity(
        start=sd_start_datetime(payload.start), end=sd_end_datetime(payload.end)
    )

    # Get the person in MO
    r_person = await gql_client.get_person(payload.cpr)
    person = only(r_person.objects)
    if person is None:
        # TODO: Return proper HTTP 5xx error message if this happens
        raise PersonNotFoundError("Could not find person in MO")

    # Get the engagement in MO
    eng = await gql_client.get_engagement_timeline(
        person=person.uuid,
        user_key=user_key,
        from_date=payload_validity.from_,
        to_date=payload_validity.to,
    )
    obj = only(eng.objects)
    if obj is None:
        # TODO: Return proper HTTP 5xx error message if this happens
        raise EngagementNotFoundError("Could not find engagement in MO")

    for validity in obj.validities:
        update_payload = EngagementUpdateInput(
            uuid=obj.uuid,
            user_key=user_key,
            primary=validity.primary.uuid if validity.primary is not None else None,
            validity=get_update_validity(
                validity.validity.from_, validity.validity.to, payload_validity
            ),
            extension_1=validity.extension_1,
            extension_2=validity.extension_2,
            extension_3=validity.extension_3,
            extension_4=validity.extension_4,
            extension_5=validity.extension_5,
            extension_6=validity.extension_6,
            extension_7=validity.extension_7,
            extension_8=validity.extension_8,
            extension_9=validity.extension_9,
            extension_10=validity.extension_10,
            person=person.uuid,
            org_unit=payload.org_unit_uuid,
            engagement_type=validity.engagement_type.uuid,
            job_function=validity.job_function.uuid,
        )
        logger.debug("Moving engagement...", payload=update_payload.dict())
        if not dry_run:
            await gql_client.update_engagement(update_payload)
    logger.debug("Engagement moved successfully")
