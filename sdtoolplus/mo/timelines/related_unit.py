from datetime import datetime
from itertools import pairwise
from typing import Sequence

from more_itertools import collapse
from more_itertools import first

from sdtoolplus.autogenerated_graphql_client import GetRelatedUnitsRelatedUnitsObjects
from sdtoolplus.autogenerated_graphql_client import OrganisationUnitFilter
from sdtoolplus.autogenerated_graphql_client import RelatedUnitFilter
from sdtoolplus.depends import GraphQLClient
from sdtoolplus.exceptions import NoValueError
from sdtoolplus.mo.timelines.common import datetime_to_mo_end
from sdtoolplus.mo.timelines.common import mo_end_to_timeline_end
from sdtoolplus.mo.timelines.org_unit import get_ou_timeline
from sdtoolplus.mo_org_unit_importer import OrgUnitUUID
from sdtoolplus.models import EngagementUnit
from sdtoolplus.models import HasValidities
from sdtoolplus.models import Timeline
from sdtoolplus.models import UnitParent


def _get_mo_objects_endpoints(
    objects: Sequence[HasValidities],
    start: datetime,
    end: datetime,
) -> list[datetime]:
    """
    Given a collection of MO objects with validities, returns the merged list of
    endpoints.

    E.g. in the case of dep3 having related units C, D and E as below, the function
    will return [t3, t4, t5, t6, t7, t8] (where t3 and t8 are the provided start
    and end argument, respectively).

    Time  --------t1--------t2----t3--t4--t5--t6--t7--t8-----t9--------------------->
    dep3          |-------------C---------|---E---|
                                      |---D---|
    """
    endpoints = set(
        collapse(
            (validity.validity.from_, mo_end_to_timeline_end(validity.validity.to))
            for obj in objects
            for validity in obj.validities
        )
    )
    endpoints = endpoints.union({start, end})

    return sorted(endpoint for endpoint in endpoints if start <= endpoint <= end)


def _get_related_unit_at(
    objects: list[GetRelatedUnitsRelatedUnitsObjects],
    unit_uuid: OrgUnitUUID,
    at: datetime,
) -> OrgUnitUUID | None:
    """
    Get a single related unit (or None if no such unit exists) at the given time. If
    there are more than one related unit, we select the first in a list of sorted org
    unit UUIDs
    """
    related_units = sorted(
        org_unit_uuid
        for obj in objects
        for validity in obj.validities
        for org_unit_uuid in validity.org_unit_uuids
        if not org_unit_uuid == unit_uuid
        and validity.validity.from_ <= at < mo_end_to_timeline_end(validity.validity.to)
    )
    return first(related_units, default=None)


async def _resolve_via_parents(
    gql_client: GraphQLClient,
    unit_uuid: OrgUnitUUID,
    start: datetime,
    end: datetime,
    unknown_unit_uuid: OrgUnitUUID,
) -> list[EngagementUnit]:
    """
    Returns related units by walking up the org tree, accounting for the infinitely
    expanding universe of temporal parents.
    """
    ou_timeline = await get_ou_timeline(
        gql_client, unit_uuid, from_date=start, to_date=end
    )

    if ou_timeline.parent == Timeline[UnitParent]():
        # We have no parents => use unknown.
        return [EngagementUnit(start=start, end=end, value=unknown_unit_uuid)]

    result: list[EngagementUnit] = []
    endpoints = sorted(
        set(
            endpoint
            for endpoint in ou_timeline.parent.get_interval_endpoints()
            if start <= endpoint < end
        ).union({start, end}),
    )
    for start_, end_ in pairwise(endpoints):
        try:
            parent = ou_timeline.parent.entity_at(start_)
        except NoValueError:
            result.append(
                EngagementUnit(start=start_, end=end_, value=unknown_unit_uuid)
            )
            continue

        if (parent_uuid := parent.value) is None:
            result.append(
                EngagementUnit(start=start_, end=end_, value=unknown_unit_uuid)
            )
            continue

        result.extend(
            await related_units(
                gql_client,
                parent_uuid,
                start_,
                end_,
                unknown_unit_uuid,
                recursive_lookup=True,
            )
        )
    return result


async def _resolve_interval(
    gql_client: GraphQLClient,
    unit_uuid: OrgUnitUUID,
    start: datetime,
    end: datetime,
    objects: list[GetRelatedUnitsRelatedUnitsObjects],
    unknown_unit_uuid: OrgUnitUUID,
    recursive_lookup: bool,
) -> list[EngagementUnit]:
    """
    Resolve a single (start, end) chunk: prefer a direct related unit, otherwise
    either give up with "unknown" or recurse via parents.
    """
    ou_related_unit = _get_related_unit_at(
        objects=objects, unit_uuid=unit_uuid, at=start
    )
    if ou_related_unit is not None:
        # Happy path. We have a related unit.
        return [EngagementUnit(start=start, end=end, value=ou_related_unit)]

    if not recursive_lookup:
        # recursive lookup is disabled, so just give up and return unknown
        return [EngagementUnit(start=start, end=end, value=unknown_unit_uuid)]

    return await _resolve_via_parents(
        gql_client=gql_client,
        unit_uuid=unit_uuid,
        start=start,
        end=end,
        unknown_unit_uuid=unknown_unit_uuid,
    )


async def related_units(
    gql_client: GraphQLClient,
    unit_uuid: OrgUnitUUID,
    start: datetime,
    end: datetime,
    unknown_unit_uuid: OrgUnitUUID,
    recursive_lookup: bool,
) -> list[EngagementUnit]:
    """
    Returns the related units in the given interval (or the "Unknown" unit if no related
    unit can be found). Note that the input interval may be divided into smaller
    intervals.
    """
    mo_rel_units = await gql_client.get_related_units(
        RelatedUnitFilter(
            from_date=start,
            # This to_date is counterintuitive for this OU relation look up,
            # since the to_date is the day *after* the relation potentially
            # ends, but MO requires these dates. Especially since we are not
            # allowed to ask for an OU relation where from_date=to_date, which
            # is the case for a unit_interval lasting only for a single day.
            to_date=datetime_to_mo_end(end),
            org_unit=OrganisationUnitFilter(uuids=[unit_uuid]),
        )
    )

    objects = mo_rel_units.objects
    endpoints = _get_mo_objects_endpoints(objects=objects, start=start, end=end)

    result: list[EngagementUnit] = []
    for chunk_start, chunk_end in pairwise(endpoints):
        result.extend(
            await _resolve_interval(
                gql_client=gql_client,
                unit_uuid=unit_uuid,
                start=chunk_start,
                end=chunk_end,
                objects=objects,
                unknown_unit_uuid=unknown_unit_uuid,
                recursive_lookup=recursive_lookup,
            )
        )
    return result
