from uuid import UUID

import structlog

from sdtoolplus.autogenerated_graphql_client import EmployeeFilter
from sdtoolplus.autogenerated_graphql_client import EngagementFilter
from sdtoolplus.autogenerated_graphql_client import ManagerFilter
from sdtoolplus.depends import GraphQLClient
from sdtoolplus.mo.timelines.common import mo_end_to_timeline_end
from sdtoolplus.models import Active
from sdtoolplus.models import ManagerTimeline
from sdtoolplus.models import ManagerUnit
from sdtoolplus.models import Timeline
from sdtoolplus.models import combine_intervals

logger = structlog.stdlib.get_logger()


async def get_manager_timeline(
    gql_client: GraphQLClient,
    person: UUID,
    engagement: UUID,
) -> ManagerTimeline:
    gql_timeline = await gql_client.get_manager_timeline(
        ManagerFilter(
            employee=EmployeeFilter(uuids=[person]),
            engagement=EngagementFilter(uuids=[engagement]),
            from_date=None,
            to_date=None,
        )
    )

    objects = gql_timeline.objects
    if not objects:
        return ManagerTimeline()

    validities = [
        validity
        for obj in objects
        for validity in obj.validities
        # Remove vacant managers
        if validity.employee_uuid == person
    ]

    active_intervals = tuple(
        Active(
            start=validity.validity.from_,
            end=mo_end_to_timeline_end(validity.validity.to),
            value=True,
        )
        for validity in validities
    )

    unit_intervals = tuple(
        ManagerUnit(
            start=validity.validity.from_,
            end=mo_end_to_timeline_end(validity.validity.to),
            value=validity.org_unit_uuid,
        )
        for validity in validities
    )

    timeline = ManagerTimeline(
        manager_active=Timeline[Active](intervals=combine_intervals(active_intervals)),
        manager_unit=Timeline[ManagerUnit](intervals=combine_intervals(unit_intervals)),
    )
    logger.info("MO manager timeline", timeline=timeline.dict())

    return timeline
