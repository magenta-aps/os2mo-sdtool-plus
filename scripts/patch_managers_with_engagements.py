CSV_HEADER = (
    "manager_uuid",
    "person_uuid",
    "start",
    "end",
    "engagement_uuid",
)


class PersonEngagement(Interval[Optional[UUID]]):
    pass


async def get_manager_timeline(
    gql_client: GraphQLClient, manager_uuid: UUID
) -> ManagerTimeline:
    gql_timeline = await gql_client.get_manager_timeline(
        ManagerFilter(
            manager_uuid=manager_uuid,
            from_date=None,
            to_date=None,
        )
    )

    objects = gql_timeline.objects
    if not objects:
        return ManagerTimeline()

    validities = [validity for obj in objects for validity in obj.validities]

    active_intervals = tuple(
        Active(
            start=validity.validity.from_,
            end=mo_end_to_timeline_end(validity.validity.to),
            value=True,
        )
        for validity in validities
    )

    user_key_intervals = tuple(
        ManagerUserKey(
            start=validity.validity.from_,
            end=mo_end_to_timeline_end(validity.validity.to),
            value=validity.user_key,
        )
        for validity in validities
    )

    unit_intervals = tuple(
        ManagerUnit(
            start=validity.validity.from_,
            end=mo_end_to_timeline_end(validity.validity.to),
            value=validity.org_unit_uuid,
        )
        for validity in validities
    )

    person_intervals = tuple(
        ManagerPerson(
            start=validity.validity.from_,
            end=mo_end_to_timeline_end(validity.validity.to),
            value=validity.employee_uuid,
        )
        for validity in validities
    )

    engagement_intervals = tuple(
        ManagerEngagement(
            start=validity.validity.from_,
            end=mo_end_to_timeline_end(validity.validity.to),
            value=validity.engagement_response.uuid
            if validity.engagement_response is not None
            else None,
        )
        for validity in validities
    )

    type_intervals = tuple(
        ManagerType(
            start=validity.validity.from_,
            end=mo_end_to_timeline_end(validity.validity.to),
            value=validity.manager_type_uuid,
        )
        for validity in validities
    )

    level_intervals = tuple(
        ManagerLevel(
            start=validity.validity.from_,
            end=mo_end_to_timeline_end(validity.validity.to),
            value=validity.manager_level_uuid,
        )
        for validity in validities
    )

    responsibility_intervals = tuple(
        ManagerResponsibility(
            start=validity.validity.from_,
            end=mo_end_to_timeline_end(validity.validity.to),
            value=list(sorted(validity.responsibility_uuids))
            if validity.responsibility_uuids is not None
            else None,
        )
        for validity in validities
    )

    timeline = ManagerTimeline(
        manager_active=Timeline[Active](intervals=combine_intervals(active_intervals)),
        manager_user_key=Timeline[ManagerUserKey](
            intervals=combine_intervals(user_key_intervals)
        ),
        manager_unit=Timeline[ManagerUnit](intervals=combine_intervals(unit_intervals)),
        manager_person=Timeline[ManagerPerson](
            intervals=combine_intervals(person_intervals)
        ),
        manager_engagement=Timeline[ManagerEngagement](
            intervals=combine_intervals(engagement_intervals)
        ),
        manager_type=Timeline[ManagerType](intervals=combine_intervals(type_intervals)),
        manager_level=Timeline[ManagerLevel](
            intervals=combine_intervals(level_intervals)
        ),
        manager_responsibilities=Timeline[ManagerResponsibility](
            intervals=combine_intervals(responsibility_intervals)
        ),
    )
    logger.info("MO manager timeline", timeline=timeline.dict())

    return timeline


@alru_cache()
async def get_person_engagement_timelines(
    gql_client: GraphQLClient, person_uuid: UUID
) -> list[Timeline[PersonEngagement]]:
    gql_timeline = await gql_client.get_engagement_timeline(
        EngagementFilter(
            employee=EmployeeFilter(uuids=[person_uuid]),
            from_date=None,
            to_date=None,
        )
    )

    objects = gql_timeline.objects
    if not objects:
        return []

    timelines = []
    for object_ in objects:
        intervals = tuple(
            PersonEngagement(
                start=validity.validity.from_,
                end=mo_end_to_timeline_end(validity.validity.to),
                value=object_.uuid,
            )
            for validity in object_.validities
        )

        timelines.append(
            Timeline[PersonEngagement](intervals=combine_intervals(intervals))
        )

    return timelines


def get_engagement_uuids_in_interval(
    person_engagement_timelines: list[Timeline[PersonEngagement]],
    at: datetime,
) -> list[UUID]:
    engagement_uuids = []
    for timeline in person_engagement_timelines:
        try:
            eng_uuid = timeline.entity_at(at).value
        except NoValueError:
            continue

        assert eng_uuid is not None
        engagement_uuids.append(eng_uuid)

    return engagement_uuids  # type: ignore  # my can't understand the generic types


async def update_manager(
    gql_client: GraphQLClient,
    manager_uuid: UUID,
    manager_timeline: ManagerTimeline,
    engagement_uuid: UUID,
    start: datetime,
    end: datetime,
) -> None:
    # Yes, we use the mutator from the testing section 😎
    await gql_client._testing__update_manager(
        ManagerUpdateInput(
            uuid=manager_uuid,
            validity=timeline_interval_to_mo_validity(start, end),
            user_key=manager_timeline.manager_user_key.entity_at(start).value,
            person=manager_timeline.manager_person.entity_at(start).value,
            engagement=engagement_uuid,
            responsibility=manager_timeline.manager_responsibilities.entity_at(
                start
            ).value,
            org_unit=manager_timeline.manager_unit.entity_at(start).value,
            manager_type=manager_timeline.manager_type.entity_at(start).value,
            manager_level=manager_timeline.manager_level.entity_at(start).value,
        )
    )


def write_csv(path: Path, rows: list[tuple[str, ...]]) -> None:
    with open(path, "w", newline="") as fp:
        writer = csv.writer(fp)
        writer.writerow(CSV_HEADER)
        writer.writerows(rows)


async def run(
    write_to_mo: bool,
    man_uuid: UUID | None,
    updated_csv: Path,
    not_updated_csv: Path,
) -> None:
    gql_client = get_gql_client()

    managers = await gql_client.get_managers(
        ManagerFilter(from_date=None, to_date=None)
    )
    manager_uuids = [manager.uuid for manager in managers.objects]
    if man_uuid is not None:
        manager_uuids = [man_uuid]

    # Keep track of which managers we are updating and which not
    updated: list[tuple[str, ...]] = []
    not_updated: list[tuple[str, ...]] = []

    for manager_uuid in manager_uuids:
        manager_timeline = await get_manager_timeline(gql_client, manager_uuid)

        endpoints = sorted(manager_timeline.get_interval_endpoints())
        for start, end in pairwise(endpoints):
            if manager_timeline.manager_engagement.entity_at(start).value is not None:
                # We already have an engagement reference in this interval
                continue

            person_uuid = manager_timeline.manager_person.entity_at(start).value
            if person_uuid is None:
                # Skip intervals with vacant managers
                continue

            # If we reach this line, we have to update the manager engagement field

            person_engagement_timelines = await get_person_engagement_timelines(
                gql_client, person_uuid
            )

            engagement_endpoints = sorted(
                set(
                    endpoint
                    for timeline in person_engagement_timelines
                    for endpoint in timeline.get_interval_endpoints()
                    if start <= endpoint <= end
                ).union({start, end})
            )

            for start_, end_ in pairwise(engagement_endpoints):
                engagement_uuids = get_engagement_uuids_in_interval(
                    person_engagement_timelines, start_
                )
                if len(engagement_uuids) > 1:
                    not_updated.append(
                        (
                            str(manager_uuid),
                            str(person_uuid),
                            start_.isoformat(),
                            end_.isoformat(),
                            "More than one manager engagement",
                        )
                    )

                try:
                    engagement_uuid = one(engagement_uuids)
                except ValueError:
                    not_updated.append(
                        (
                            str(manager_uuid),
                            str(person_uuid),
                            start_.isoformat(),
                            end_.isoformat(),
                            "No manager engagement found",
                        )
                    )
                else:
                    msg = str(engagement_uuid)
                    if write_to_mo:
                        try:
                            await update_manager(
                                gql_client=gql_client,
                                manager_uuid=manager_uuid,
                                manager_timeline=manager_timeline,
                                engagement_uuid=engagement_uuid,
                                start=start_,
                                end=end_,
                            )
                        except GraphQLClientGraphQLMultiError:
                            msg = "GraphQL error"

                    updated.append(
                        (
                            str(manager_uuid),
                            str(person_uuid),
                            start_.isoformat(),
                            end_.isoformat(),
                            msg,
                        )
                    )

    write_csv(updated_csv, updated)
    write_csv(not_updated_csv, not_updated)

    logger.info(
        "Wrote CSV files",
        updated_csv=str(updated_csv),
        updated_rows=len(updated),
        not_updated_csv=str(not_updated_csv),
        not_updated_rows=len(not_updated),
    )


@click.command()
@click.option("--write-to-mo", type=click.BOOL, default=False)
@click.option("--manager", "man_uuid", type=click.UUID)
@click.option(
    "--updated-csv",
    type=click.Path(dir_okay=False, writable=True, path_type=Path),
    default=Path("/tmp/updated_managers.csv"),
    show_default=True,
    help="CSV output file for manager intervals that were updated",
)
@click.option(
    "--not-updated-csv",
    type=click.Path(dir_okay=False, writable=True, path_type=Path),
    default=Path("/tmp/not_updated_managers.csv"),
    show_default=True,
    help="CSV output file for manager intervals that could not be updated",
)
def main(
    write_to_mo: bool,
    man_uuid: UUID | None,
    updated_csv: Path,
    not_updated_csv: Path,
) -> None:
    logger.info("Script started")
    asyncio.run(run(write_to_mo, man_uuid, updated_csv, not_updated_csv))
    logger.info("Script finished!")


if __name__ == "__main__":
    main()
