ENGAGEMENTS_CSV = "/tmp/engagements.csv"
DUBLETS_CSV = "/tmp/dublets.csv"
TERMINATED_CSV = "/tmp/terminated.csv"

logger = structlog.stdlib.get_logger()


def get_mo_engagements_from_csv(institution_identifier: str) -> list[Engagement]:
    with open(ENGAGEMENTS_CSV, newline="") as fp:
        reader = csv.DictReader(fp)
        return [
            Engagement(
                institution_identifier=institution_identifier,
                cpr=last(row["cpr"].split(":")),
                employment_identifier=row["user_key"],
            )
            for row in reader
        ]


def get_sd_employment(
    sd_client: SDClient, engagement: Engagement
) -> EngagementTimeline | None:
    try:
        r_employment = sd_client.get_employment_changed(
            GetEmploymentChangedRequest(
                InstitutionIdentifier=engagement.institution_identifier,
                PersonCivilRegistrationIdentifier=engagement.cpr,
                EmploymentIdentifier=engagement.employment_identifier,
                ActivationDate=date.min,
                DeactivationDate=date.max,
                EmploymentStatusIndicator=True,
            )
        )
    except SDEmploymentNotFound:
        logger.warning(
            "SD employment not found!",
            institution_identifier=engagement.institution_identifier,
            cpr=engagement.cpr,
            emp_id=engagement.employment_identifier,
        )
        return None
    except SDRootElementNotFound as sd_error:
        logger.warning(
            "Could not read employment from SD",
            institution_identifier=engagement.institution_identifier,
            cpr=engagement.cpr,
            emp_id=engagement.employment_identifier,
            error=sd_error.error,
        )
        return None
    else:
        sd_employment_timeline = get_employment_timeline(
            r_employment,
            use_sd_status_codes_as_engagement_types=False,
        )
        return sd_employment_timeline


async def get_mo_person(gql_client: GraphQLClient, cpr: str) -> UUID | None:
    # Get the person
    r_person = await gql_client.get_person(CPRNumber(cpr))
    try:
        return one(r_person.objects).uuid
    except ValueError as error:
        logger.error(
            "Not exactly one person found in MO",
            cpr=cpr,
            error=error,
        )
        return None


async def run(not_dry_run: bool, start: int) -> None:
    settings = get_settings()

    sd_client = SDClient(settings.sd_username, settings.sd_password.get_secret_value())
    gql_client = get_gql_client()

    mo_engagements = get_mo_engagements_from_csv(settings.sd_institution_identifier)
    mo_engagements_without_cpr_iter, mo_engagements_iter = partition(
        lambda eng: bool(re.fullmatch(r"^\d{10}$", eng.cpr)), mo_engagements
    )
    logger.info("No CPRs", engs=list(mo_engagements_without_cpr_iter))
    mo_engagements = list(mo_engagements_iter)

    with open(TERMINATED_CSV, "a", buffering=1) as fp:
        for i, mo_eng in enumerate(mo_engagements[start:], start=start):
            logger.info(
                f"Processing engagement ({i}/{len(mo_engagements)})",
                engagement=mo_eng.dict(),
            )

            try:
                sd_eng_timeline = get_sd_employment(sd_client, mo_eng)
            except SDCallError:
                logger.warn("Problem calling SD", engagement=mo_eng.dict())
                continue
            if sd_eng_timeline is None:
                continue

            mo_person = await get_mo_person(gql_client, mo_eng.cpr)
            if mo_person is None:
                continue

            try:
                mo_eng_timeline = await get_engagement_timeline(
                    gql_client=gql_client,
                    person=mo_person,
                    user_key=mo_eng.employment_identifier,
                )
            except MoreThanOneEngagementError:
                with open(DUBLETS_CSV, "a") as dublets_fp:
                    dublets_fp.write(
                        f"{str(mo_person)},{mo_eng.employment_identifier}\n"
                    )
                continue
            if mo_eng_timeline == EngagementTimeline():
                continue

            endpoints = sorted(
                sd_eng_timeline.eng_active.get_interval_endpoints().union(
                    mo_eng_timeline.eng_active.get_interval_endpoints()
                )
            )
            for start_, end in pairwise(endpoints):
                try:
                    sd_is_active = cast(
                        bool, sd_eng_timeline.eng_active.entity_at(start_).value
                    )
                except NoValueError:
                    sd_is_active = False

                try:
                    mo_is_active = cast(
                        bool, mo_eng_timeline.eng_active.entity_at(start_).value
                    )
                except NoValueError:
                    mo_is_active = False

                if mo_is_active and not sd_is_active:
                    fp.write(
                        f"{str(mo_person)},{mo_eng.employment_identifier},{start_.isoformat()},{end.isoformat()}\n"
                    )
                    if not_dry_run:
                        await terminate_engagement(
                            gql_client=gql_client,
                            person=mo_person,
                            user_key=mo_eng.employment_identifier,
                            start=start_,
                            end=end,
                        )


@click.command()
@click.option("--not-dry-run", is_flag=True)
@click.option("--start", type=click.INT, default=0)
def main(not_dry_run: bool, start: int) -> None:
    logger.info("Script started")
    asyncio.run(run(not_dry_run, start))
    logger.info("Script finished")


if __name__ == "__main__":
    main()
